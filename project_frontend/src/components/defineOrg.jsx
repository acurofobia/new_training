export const defineOrg = (org) => {
  if (org == "fda") {
    return "Федеральное дорожное агентство"
  }
  if (org == "favt_mos") {
    return "Федеральное агентство воздушного транспорта(Москва)"
  }
  if (org == "favt_ul") {
    return "Федеральное агентство воздушного транспорта(Ульяновск)"
  }
  if (org == "fazt") {
    return "Федеральное агентство железнодорожного транспорта"
  }
} 